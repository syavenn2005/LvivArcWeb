export function getMonumentById(id: string): Monument | undefined {
  return monuments.find(monument => monument.id === id)
}

export function getAllMonumentIds(): string[] {
  return monuments.map(monument => monument.id)
}

export function getMonumentsByCategory(category: Category): Monument[] {
  return monuments.filter(monument => monument.category === category)
}

export function getAllCategories(): Category[] {
  return ['churches', 'defensive', 'civil']
}
